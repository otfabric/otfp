package s7

import "testing"

func FuzzValidateS7Response(f *testing.F) {
	// Valid S7 Ack-Data in TPKT/COTP DT wrapper.
	f.Add([]byte{
		0x03, 0x00, 0x00, 0x1B, // TPKT
		0x02, 0xF0, 0x80, // COTP DT
		0x32, 0x03, 0x00, 0x00, 0x00, 0x01, 0x00, 0x08, 0x00, 0x00, // S7 header
		0x00, 0x00, // Error class/code
		0xF0, 0x00, 0x00, 0x01, 0x00, 0x01, 0x01, 0xE0, // Params
	})
	// Wrong S7 protocol ID.
	f.Add([]byte{
		0x03, 0x00, 0x00, 0x0F,
		0x02, 0xF0, 0x80,
		0x33, 0x03, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00,
	})
	// Too short.
	f.Add([]byte{0x03, 0x00, 0x00, 0x07, 0x02, 0xF0, 0x80})
	// Empty.
	f.Add([]byte{})

	f.Fuzz(func(t *testing.T, data []byte) {
		result := validateS7Response(data, 0.35, "baseline")
		if result.Confidence < 0 || result.Confidence > 1.0 {
			t.Errorf("confidence out of range: %f", result.Confidence)
		}
	})
}
